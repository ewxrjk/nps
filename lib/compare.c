#include <config.h>
#include "format.h"
#include "compare.h"

struct procinfo *pi;

int compare_pid(const void *av, const void *bv) {
  pid_t a = *(const pid_t *)av;
  pid_t b = *(const pid_t *)bv;
  return format_compare(pi, a, b);
}
